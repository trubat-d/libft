#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	if (lst != NULL)
	{
		i = 0;
		while (lst->next)
		{
			i++;
			lst = lst->next;
		}
		i++;
		return (i);
	}
	else
	{
		return (0);
	}
}
